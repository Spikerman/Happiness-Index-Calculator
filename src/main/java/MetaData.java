public class MetaData {
    double avgLatitude;
    double avgLongitude;
    double value;

    public MetaData(double avgLatitude, double avgLongitude, double value) {
        this.avgLatitude = avgLatitude;
        this.avgLongitude = avgLongitude;
        this.value = value;
    }
}
