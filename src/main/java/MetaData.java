public class MetaData {
    double avgLatitude;
    double avgLongitude;
    int weight;

    public MetaData(double avgLatitude, double avgLongitude, int weight) {
        this.avgLatitude = avgLatitude;
        this.avgLongitude = avgLongitude;
        this.weight = weight;
    }
}
